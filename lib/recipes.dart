import 'dart:async';

import 'package:flutter/material.dart';

import 'src/widgets.dart';

class recipes extends StatefulWidget {
  const recipes({required this.addMessage, super.key});

  final FutureOr<void> Function(String message) addMessage;

  @override
  State<recipes> createState() => _recipesBookState();
}

class _recipesBookState extends State<recipes> {
  final _formKey = GlobalKey<FormState>(debugLabel: '_recipesBookState');
  final _controller = TextEditingController();

  @override
  Widget build(BuildContext context) {
    return Padding(
      padding: const EdgeInsets.all(8.0),
      child: Form(
        key: _formKey,
        child: Row(
          children: [
            Expanded(
              child: TextFormField(
                controller: _controller,
                decoration: const InputDecoration(
                  hintText: 'Leave a messafg',
                ),
                validator: (value) {
                  if (value == null || value.isEmpty) {
                    return 'Enter your message to continue';
                  }
                  return null;
                },
              ),
            ),
            const SizedBox(width: 8),
            StyledButton(
              onPressed: () async {
                if (_formKey.currentState!.validate()) {
                  await widget.addMessage(_controller.text);
                  _controller.clear();
                }
              },
              child: Row(
                children: const [
                  Icon(Icons.send),
                  SizedBox(width: 4),
                  Text('SEND'),
                ],
              ),
            ),
          ],
        ),
      ),
    );
  }
}